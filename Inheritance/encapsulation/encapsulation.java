package Inheritance.encapsulation;

public class encapsulation {
    private int age;
    private String name;

    public void setAge(int age){
        this.age=age;
    }
    public void setname(String name){
        this.name=name;
    }

    public int getAge(){
        return age;
    }
    public String getname(){
        return  name;
    }
}

